    public static class EnumerableExtensions
    {
        public static Dictionary<TKey, T> Map<TKey, T>(this IEnumerable<T> collection, Func<T, TKey> keyfunc)
        {
            var d = new Dictionary<TKey, T>();
            collection.ForEach(x =>
            {
                var key = keyfunc(x);
                if (!d.ContainsKey(key))
                {
                    d.Add(keyfunc(x), x);
                }
            });
            return d;
        }
    }

